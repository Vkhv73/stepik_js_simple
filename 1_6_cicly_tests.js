for (i = 1; i <= 5; i = i + 2) {
    console.log("i = " + i);
}
// Результатом выполнения данного цикла будет пятикратное выведение значения переменной i в виде i = 1, i = 2и т.д.
//
// Замечание:
// Немного забегая вперед, поясним - с помощью + можно объединять строки.
// В примере выше, в конструкции "i = " + i мы к строке прибавляем число, которое "приводится" к строковому типу данных
// и в такой ситуации, если i = 1, получаем в результате строку "i = 1".